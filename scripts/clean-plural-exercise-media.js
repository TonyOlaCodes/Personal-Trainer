/**
 * Remove plural exercise media keys when singular exists; copy media if singular lacks it.
 * Run: node scripts/clean-plural-exercise-media.js
 */
const fs = require("fs");
const path = require("path");
const { EXERCISE_PLURAL_MERGES } = require("./exercisePluralMerges");

const mediaPath = path.join(__dirname, "exerciseMediaData.js");
const media = require("./exerciseMediaData.js");

for (const [plural, singular] of EXERCISE_PLURAL_MERGES) {
    const pluralMedia = media[plural];
    if (!pluralMedia) continue;

    if (!media[singular]) {
        media[singular] = pluralMedia;
    } else {
        const s = media[singular];
        media[singular] = {
            ...pluralMedia,
            ...s,
            videoUrl: s.videoUrl ?? pluralMedia.videoUrl,
            thumbnailUrl: s.thumbnailUrl ?? pluralMedia.thumbnailUrl,
            instructions: s.instructions ?? pluralMedia.instructions,
            sourceUrl: s.sourceUrl ?? pluralMedia.sourceUrl,
        };
    }

    delete media[plural];
}

const sortedKeys = Object.keys(media).sort((a, b) => a.localeCompare(b));
const lines = sortedKeys.map((key) => {
    const value = media[key];
    const json = JSON.stringify(value, null, 4);
    const indented = json.split("\n").map((line, i) => (i === 0 ? line : `    ${line}`)).join("\n");
    return `    ${JSON.stringify(key)}: ${indented.trimStart()}`;
});

const output = `/** Auto-generated by scripts/generate-exercise-media.mjs — plural keys merged via clean-plural-exercise-media.js */\nmodule.exports = {\n${lines.join(",\n")}\n};\n`;

fs.writeFileSync(mediaPath, output);
console.log(`Cleaned ${EXERCISE_PLURAL_MERGES.length} plural merge pairs from exerciseMediaData.js`);
console.log(`Remaining entries: ${sortedKeys.length}`);
